  document.addEventListener('DOMContentLoaded', () => {
    const contenedor = document.getElementById('datos-contenedor');
  
    fetch('datos.html')
        .then(response => response.text())
        .then(html => {
            datos.forEach(dato => {
                const tempDiv = document.createElement('div');
                tempDiv.innerHTML = html.trim();
                const elemento = tempDiv.firstElementChild || tempDiv.firstChild;
  
                // Verificar que elemento es un nodo de DOM válido
                if (!(elemento instanceof HTMLElement)) {
                    console.error('Elemento no es un nodo de DOM válido:', elemento);
                    return;
                }
  
                // Verificaciones adicionales para asegurarse de que los elementos existen
                const tituloElem = elemento.querySelector('.titulo');
                const subtituloElem = elemento.querySelector('.subtitulo');
                const descripcionElem = elemento.querySelector('.descripcion');
                const imagenElem = elemento.querySelector('.imagen');
  
                if (tituloElem && subtituloElem && descripcionElem && imagenElem) {
                    tituloElem.textContent = dato.titulo;
                    subtituloElem.textContent = dato.subtitulo;
                    descripcionElem.textContent = dato.descripcion;
  
                    // Verificar si la imagen existe
                    const imgPath = `../public/img/${dato.img}`;
                    fetch(imgPath)
                        .then(response => {
                            if (response.ok) {
                                imagenElem.src = imgPath;
                            } else {
                                imagenElem.src = 'ruta/a/imagen/default.jpg'; // Reemplaza con la ruta a tu imagen por defecto
                            }
                            imagenElem.alt = dato.titulo;
                        });
  
                    contenedor.appendChild(elemento);
                } else {
                    console.error('No se encontraron todos los elementos en el HTML cargado:', { tituloElem, subtituloElem, descripcionElem, imagenElem });
                }
            });
        })
        .catch(error => console.error('Error al cargar datos.html:', error));
  });
  
  
  
  